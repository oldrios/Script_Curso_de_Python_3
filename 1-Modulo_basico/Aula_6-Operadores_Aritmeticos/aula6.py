'''
+ Soma, - Subtração, * Multiplicação, / Divisão, // Divisão para inteiro, ** Exponenciação
% Resto da divisão, () Alterar a precedência
'''

# Soma/Adição
print('Soma "+" (10+3) =', 10+3)

# Multiplicação
print('Multiplicação "*" (10*3) =', 10*3)

# Subtração
print('Subtração "-" (10-3) =', 10-3)

# Divisão
print('Divisão "/" (10/3) =', 10/3)

# Divisão para número inteiro
print('Divisão para número inteiro "//" (10//3) =', 10//3)

# Exponenciação
print('Exponenciação "**" (10**3) =', 10**3)

# Resto da divisão/Módulo
print('Resto da divisão "%" (10%3) =', 10%3)
print('Resto da divisão "%" (8%3) =', 8%2)
print('Resto da divisão "%" (8%3) =', 8%2)

''' Utilizando operadores aritmeticos com strings '''

# Utilizando operador de multiplicação com string
print('Concatenador com "+" ("10"+"3") =','10'+'3')  # Ao realizar essa operação, o '+' terá a função de concatenador com a string

# Utilizando operador de multiplicação com string
print('Repetição com "*" (10*A) =', 10*'A')  # Ao realizar essa operação, o '*' terá a função de repetidor com a string

